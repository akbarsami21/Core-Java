package Pattern;
import java.util.*;

public class invertedHalfPyramid2 {
 public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	int n,i,j;
	System.out.print("Enter Numer:");
	n=sc.nextInt();
	for(i=1;i<=n;i++) {
		for(j=i;j<=n;j++) {
			System.out.print("*");
		}
		System.out.println();
	
	
	}
	
}
}
